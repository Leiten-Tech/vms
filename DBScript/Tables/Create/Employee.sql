CREATE TABLE IF NOT EXISTS `Employee` (
  `Employee_Id` BIGINT AUTO_INCREMENT NOT NULL,
  `Plant_Id` BIGINT NOT NULL,
  `Employee_Code` VARCHAR(20) NULL DEFAULT NULL,
  `First_Name` VARCHAR(255) NULL DEFAULT NULL,
  `Last_Name` VARCHAR(255) NULL DEFAULT NULL,
  `Dob` DATE NULL DEFAULT NULL,
  `Age` SMALLINT NOT NULL,
  `Designation_Id` BIGINT NOT NULL,
  `Dept_Id` BIGINT NOT NULL,
  `Email` VARCHAR(255) NULL DEFAULT NULL,
  `Primary_Mobile_No` VARCHAR(15) NULL DEFAULT NULL,
  `Secondary_Mobile_No` VARCHAR(15) NULL DEFAULT NULL,
  `Marital_Status` INT NOT NULL,
  `Gender` INT NOT NULL,
  `Emp_Type_Id` INT NOT NULL,
  `Biometric_Id` INT NULL DEFAULT NULL,
  `Idcard_No` VARCHAR(20) NOT NULL,
  `Blood_Group` INT NOT NULL,
  `Date_Of_Joining` DATE NULL DEFAULT NULL,
  `Releaving_Date` DATE NULL DEFAULT NULL,
  `Reporting_Person` BIGINT NOT NULL,
  `Image_Name` LONGTEXT NULL DEFAULT NULL,
  `Image_Url` LONGTEXT NULL DEFAULT NULL,
  `Status` INT NULL DEFAULT NULL,
  `Created_By` INT NOT NULL,
  `Created_On` DATETIME(6) NOT NULL,
  `Modified_By` INT NULL DEFAULT NULL,
  `Modified_On` DATETIME(6) NULL DEFAULT NULL,
  `Company_Id` BIGINT NULL DEFAULT NULL,
  `Address` VARCHAR(500) NULL DEFAULT NULL,
  PRIMARY KEY (`Employee_Id`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4
COLLATE = utf8mb4_general_ci;
