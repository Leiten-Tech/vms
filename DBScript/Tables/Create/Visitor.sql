CREATE TABLE IF NOT EXISTS `Visitor` (
  `Visitor_Id` BIGINT AUTO_INCREMENT NOT NULL,
  `Visitor_Code` VARCHAR(50) NOT NULL,
  `Visitor_Type_Id` BIGINT NOT NULL,
  `Company_Id` BIGINT NOT NULL,
  `Plant_Id` BIGINT NOT NULL,
  `Country_Id` BIGINT NULL DEFAULT NULL,
  `State_Id` BIGINT NULL DEFAULT NULL,
  `City_Id` BIGINT NULL DEFAULT NULL,
  `Title_id` INT NOT NULL,
  `First_Name` VARCHAR(255) NOT NULL,
  `Last_Name` VARCHAR(255) NULL DEFAULT NULL,
  `Dob` DATETIME(6) NOT NULL,
  `Visitor_Company` VARCHAR(255) NULL DEFAULT NULL,
  `Address` LONGTEXT NULL DEFAULT NULL,
  `Mail_Id` VARCHAR(255) NULL,
  `Mobile_No` VARCHAR(255) NOT NULL,
  `Id_Card_Type` INT NULL DEFAULT NULL,
  `Id_Card_No` VARCHAR(255) NULL DEFAULT NULL,
  `Document_Name` LONGTEXT NOT NULL,
  `Document_Url` LONGTEXT NOT NULL,
  `Visitor_Document_Name` LONGTEXT NULL DEFAULT NULL,
  `Visitor_Document_Url` LONGTEXT NULL DEFAULT NULL,
  `Status` INT NOT NULL,
  `Created_By` INT NOT NULL,
  `Created_On` DATETIME(6) NOT NULL,
  `Modified_By` INT NULL DEFAULT NULL,
  `Modified_On` DATETIME(6) NULL DEFAULT NULL,
  PRIMARY KEY (`Visitor_Id`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4
COLLATE = utf8mb4_general_ci;